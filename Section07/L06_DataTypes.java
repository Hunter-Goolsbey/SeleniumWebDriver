package Section07;

public class L06_DataTypes {
	
	public static void main(String[] args) {
		
		String dogBreed = "poodle";
		
		int number = 10;
		
		boolean flag = true;
		
		char letter = 'a';
		
		float example = 1.123f;
		
		double doubExample = 1.234;
		
		
		System.out.println(dogBreed);
		System.out.println(number);
		System.out.println(flag);
		System.out.println(letter);
		System.out.println(example);
		System.out.println(doubExample);
	}

}
